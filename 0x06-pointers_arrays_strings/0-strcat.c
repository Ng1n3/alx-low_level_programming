/**
 * _strcat - fills memory with a constant byte
 * @dest: destination
 * @src: source
 * @n: number of bytes to fill
 * Return: pointer to memory area 's'
 */

char *_strcat(char *dest, char *src)
{
	unsigned int i;

	for (i = 0; i < n; i++)
		dest[i] = src[i];
	return (dest);
}
